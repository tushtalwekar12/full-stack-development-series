// conversion Operation -
 
//ex-1 convert string to number 
let value1 = "33";
let valueInNumber = Number(value1);
console.log(valueInNumber); // Output: 33
console.log(typeof(value1)); // string
console.log(typeof(valueInNumber)); //number

